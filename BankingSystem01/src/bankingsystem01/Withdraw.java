    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        double withdrawalAmount = Double.parseDouble(withdraw.getText());

    // Validate input: Check if withdrawal amount is valid
    if (withdrawalAmount <= 0) {
        JOptionPane.showMessageDialog(this, "Invalid withdrawal amount.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Check if withdrawal amount exceeds account balance
    if (withdrawalAmount > acc.getBalance()) {
        JOptionPane.showMessageDialog(this, "Insufficient balance.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Update account balance
    acc.withdraw(withdrawalAmount);

   


    // Display feedback to the user
    JOptionPane.showMessageDialog(this, "Withdrawal successful.", "Success", JOptionPane.INFORMATION_MESSAGE);
    JOptionPane.showMessageDialog(this, "Current balance: " + acc.getBalance());
    // Hide current window and show the menu
    new Menuu(acc, database, accounts).setVisible(true);
    this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

